// creating an empty array
let myArr=[];

// adding item to the end of array
myArr.push('angel');

// logging new array to the console
console.log(myArr);

// adding a value to beginning of array
myArr.unshift(3);

//adding one more value to end
myArr.push(57);

myArr.shift() ;// removing the first item from array
myArr.pop() ;// removing the last last element from array

// logging array to console
console.log(myArr);